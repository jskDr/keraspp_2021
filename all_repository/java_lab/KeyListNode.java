public class KeyListNode {
    private int key;
    private int data;
    private KeyListNode next;
    public int getKey() {
        return key;
    }
    public void setKey(int key) {
        this.key = key;
    }
    public int getData() {
        return data;
    }
    public void setData(int data) {
        this.data = data;
    }
    public KeyListNode getNext() {
        return next;
    }
    public void setNext(KeyListNode next) {
        this.next = next;
    }
}